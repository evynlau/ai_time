# Day 03: 运算符与表达式
# 📖 + - * / 比较运算符 逻辑

print("🔢 算术运算符:")
a = 10
b = 3
print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")
print(f"{a} / {b} = {a / b}")

print("\n⚖️ 比较运算符:")
print(f"{a} > {b}? {a > b}")
print(f"{a} < {b}? {a < b}")
print(f"{a} == {a} ? {a == a}")

print("\n🔗 逻辑运算符:")
print(f"True and True = {True and True}")
print(f"True or False = {True or False}")
print(f"not False = {not False}")

# 简易计算器
print("\n🧮 简易计算器:")
num1 = 5
num2 = 7
print(f"{num1} + {num2} = {num1 + num2}")
print(f"{num1} - {num2} = {num1 - num2}")
print(f"{num1} * {num2} = {num1 * num2}")
print(f"{num1} / {num2} = {num1 / num2:.2f}")

print("\n🎉 Day 03 完成!")
