    print("🌟 第一關解答：觀念問答題 🌟")
    print("=" * 50)

    print("""
    1. while 和 for 的差別：

       for 迴圈：知道你「要執行幾次」，例如 range(5) 就是執行 5 次
       while 迴圈：不知道要執行幾次，只知道「何時該停下來」

       什麼時候用 while：
       - 當你需要等到某個條件成立才能停止時
       - 當你在處理使用者輸入，需要驗證時
       - 當你不確定要執行幾次時

    2. 為什麼會變成無限迴圈？

       因為 count 的值永遠是 1，沒有改變！
       while count <= 10: 這個條件永遠為 True（1 <= 10）
       所以迴圈永遠不會停下來！

       解決方法：加上 count += 1

    3. break 的作用：

       break 可以「強制結束」迴圈，類似緊急煞車！
       當程式執行到 break，就會立刻跳出去，不再執行迴圈裡的程式碼。
    """)


