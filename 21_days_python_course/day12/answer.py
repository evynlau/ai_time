    print("🌟 第三關解答：總和計算器 🌟")
    print("=" * 50)

    def 計算總和(n):
        total = 0      # 一開始總和是 0
        count = 1      # 計數器從 1 開始
        while count <= n:
            total = total + count  # 把 count 加到總和裡
            count += 1             # count 增加 1
        return total

    # 測試
