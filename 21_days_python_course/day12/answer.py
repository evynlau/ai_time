def 第五關解答():
    """
    📚 題目：使用 while 迴圈，請寫一個函式

    函式名稱：取得正確年齡
    功能：
    - 持續要求使用者輸入年齡
    - 如果輸入的不是有效的數字（0-150），就說「無效的年齡，請重新輸入」
    - 如果年齡在範圍內，就回傳這個年齡（轉成整數）
    """
    print("\n" + "=" * 50)
    print("🌟 第五關解答：使用者輸入驗證 🌟")
    print("=" * 50)

    def 取得正確年齡():
        while True:  # 永遠執行，直到遇到 break
            年齡字串 = input("   請輸入年齡：")

            # 檢查是否為數字
            if not 年齡字串.isdigit():
                print("   ❌ 無效的年齡！")
                continue  # 跳過這次，繼續下一次迴圈

            年齡 = int(年齡字串)  # 轉成整數

            # 檢查是否在有效範圍內
            if 年齡 < 0 or 年齡 > 150:
                print("   ❌ 無效的年齡！")
                continue

            # 如果都正確，就回傳
            print(f"   ✅ 有效！年齡是 {年齡}")
            return 年齡

    # 說明
    print("\n📝 程式說明：")
    print("""
    這個程式的重點：

    1. while True：建立一個無窮迴圈，永遠執行
    2. .isdigit()：檢查字串是否只包含數字字元
    3. continue：跳過這次迴圈的剩餘程式碼，繼續下一次
    4. return：在得到正確輸入時，回傳年齡並結束函式

    💡 為什麼要用 while True 而不是 while not 有效?
      因為一開始我們根本不知道什麼是「有效」，
      只能先假設輸入可能是錯的，一直問到對為止！
    """)

