def 第七關解答():
    """
    📚 題目：使用 while 迴圈，請寫一個函式

    函式名稱：找出質數
    功能：找出所有小於 n 的質數，回傳一個串列

    💡 質數的定義：
       - 是大於 1 的自然數
       - 只能被 1 和自己整除
       - 例如：2, 3, 5, 7, 11, 13, 17, 19, 23...
    """
    print("\n" + "=" * 50)
    print("🌟 第七關解答：找出質數（挑戰題）🌟")
    print("=" * 50)

    def 找出質數(n):
        質數串列 = []
        數字 = 2  # 從 2 開始（1 不是質數）

        while 數字 < n:
            是質數 = True  # 假設是質數

            # 檢查這個數是否能被 2 到 (自己-1) 的任何數整除
            除數 = 2
            while 除數 < 數字:
                if 數字 % 除數 == 0:
                    是質數 = False  # 如果能整除，就不是質數
                    break            # 不需要再檢查了
                除數 += 1

            if 是質數:
                質數串列.append(數字)

            數字 += 1

        return 質數串列

    # 測試
    print("\n📝 測試：")
    print(f"   找出質數(20) = {找出質數(20)}")
    print(f"   找出質數(30) = {找出質數(30)}")

    # 說明
    print("\n📝 程式說明：")
    print("""
    這個程式有兩層 while 迴圈：

    外層迴圈：遍歷每個數字（從 2 到 n-1）
    內層迴圈：檢查這個數字是否為質數

    如何判斷質數？
    - 假設這個數是質數
    - 用除數從 2 檢查到 (自己-1)
    - 如果有任何一個除數能整除它，就不是質數
    - 如果都不能整除，就是質數

    💡 優化建議：
      - 檢查到 sqrt(數字) 就够了，不需要檢查到 (自己-1)
      - 偶數（除了 2）都不需要檢查
    """)


