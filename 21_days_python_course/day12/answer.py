def 第四關解答():
    """
    📚 題目：使用 while 迴圈，請寫一個函式

    函式名稱：找倍數
    功能：找出從 1 到 max 中，所有可以被 divisor 整除的數
         把這些數存成一個串列（list）並回傳
    """
    print("\n" + "=" * 50)
    print("🌟 第四關解答：找出倍數 🌟")
    print("=" * 50)

    def 找倍數(divisor, max):
        result = []        # 建立一個空的串列
        count = divisor    # 從 divisor 開始（因為 1 不是 divisor 的倍數，除非從 1 開始）
        # 根據題目，我們要從 1 檢查到 max
        count = 1
        while count <= max:
            if count % divisor == 0:  # 如果可以被 divisor 整除
                result.append(count)   # 加入串列
            count += 1
        return result

    # 測試
    print("\n📝 測試：")
    print(f"   找倍數(3, 20) = {找倍數(3, 20)}")
    print(f"   找倍數(5, 30) = {找倍數(5, 30)}")


