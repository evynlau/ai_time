def 展示_基本while迴圈():
    """展示最基本的 while 迴圈用法"""
    print("\n📌 基本 while 迴圈範例：")
    count = 1
    while count <= 3:
        print(f"   計數：{count}")
        count += 1
    print("   迴圈結束！")


def 展示_密碼驗證():
    """展示如何用 while 做輸入驗證"""
    print("\n📌 密碼驗證範例（測試用密碼：python）：")
    # 這個函式在教學展示時會故意多做一次示範
    password = ""
    attempts = 0
    while password != "python":
        attempts += 1
        if attempts > 3:
            print("   太多次了！示範結束")
            break
        password = input(f"   嘗試 {attempts}：請輸入密碼（提示：python）\n   → ")
        if password != "python":
            print("   ❌ 錯誤！")
    if password == "python":
        print("   ✅ 登入成功！")


def 展示_倒數範例():
    """展示倒數計時"""
    print("\n📌 倒數範例（只數到 5）：")
    count = 5
    while count > 0:
        print(f"   ⏰ {count}")
        count -= 1
    print("   🎉 時間到！")


