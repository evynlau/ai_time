    import time

    print("🚀 火箭發射倒計時開始！🚀\n")

    count = 10
    while count > 0:
        print("🔥", count)
        time.sleep(1)  # 停 1 秒
        count -= 1

    print("\n🎆 發射！！！ 🚀🚀🚀")

