    while True:
        print("\n=== BMI 計算器 ===")
        height = float(input("請輸入身高（公尺）："))
        weight = float(input("請輸入體重（公斤）："))

        bmi = weight / (height ** 2)
        print("你的 BMI 是：", round(bmi, 2))

        if bmi < 18.5:
            print("📉 體重過輕，多吃一點！")
        elif bmi < 24:
            print("✅ 體重正常，很健康！")
