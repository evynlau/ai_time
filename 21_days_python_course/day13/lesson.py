print("""
我們用巢狀迴圈來統計多位學生的成績！

📋 資料結構（二維串列）：
[
  [姓名, 國文, 數學, 英文],
  [姓名, 國文, 數學, 英文],
  ...
]

📐 外層迴圈：遍歷每個學生
📐 內層迴圈：遍歷每個學生的各科成績
""")

students = [
    ["小明", 85, 92, 78],
    ["小華", 90, 88, 95],
    ["小美", 72, 85, 88],
    ["小強", 95, 91, 89],
    ["小琳", 68, 75, 82]
]

# 統計每科平均
subject_names = ["國文", "數學", "英文"]

print("\n📊 個人成績與平均：")
print("-" * 50)

for i, student in enumerate(students):
    name = student[0]
    scores = student[1:]
    
    # 計算個人平均
    total = sum(scores)
    average = total / len(scores)
    
    print(f"{name}: ", end="")
    for j, score in enumerate(scores):
        print(f"{subject_names[j]}={score}", end=" ")
    print(f"→ 平均={average:.1f}")

# 計算科目平均（需要巢狀迴圈）
print("\n📈 各科平均：")
print("-" * 30)

for j in range(len(subject_names)):
    subject_total = 0
    for i in range(len(students)):
        subject_total += students[i][j + 1]
    subject_average = subject_total / len(students)
    print(f"{subject_names[j]}的平均：{subject_average:.1f}")

