"""
🎯 Day 13：巢狀迴圈 (Nested Loops)
==============================
今天的課程要學習什麼是「巢狀迴圈」，
也就是在迴圈裡面再放一個迴圈！
這是一個非常重要又有趣的概念 🌟
"""

# ============================================================
# 📖 什麼是巢狀迴圈？
# ============================================================
# 巢狀迴圈就像俄羅斯套娃一樣，一層包一層
# 我們在外面放一個迴圈（稱為「外層迴圈」）
# 然後在裡面再放一個迴圈（稱為「內層迴圈」）

# 基本語法結構：
# for 外層變數 in range(外層範圍):
#     for 內層變數 in range(內層範圍):
#         # 執行程式碼

print("=" * 60)
print("📖 什麼是巢狀迴圈？")
print("=" * 60)
print("""
巢狀迴圈就像這個比喻：
🪆 外層迴圈：處理最外層的大娃娃
🪆 內層迴圈：處理每個大娃娃裡面的小娃娃

想像你在工廠包裝禮物盒：
📦 外層迴圈：遍歷每一個禮物盒
📦 內層迴圈：給每個禮物盒裡面放氣泡紙

每一個「外層」執行一次，
內層迴圈就要完整跑完一次！
""")

# ============================================================
# 🔰 第一個巢狀迴圈：基本範例
# ============================================================
print("\n" + "=" * 60)
print("🔰 第一個巢狀迴圈：基本範例")
print("=" * 60)

print("\n外層迴圈執行 3 次，內層迴圈執行 4 次：")
print("總共會執行 3 x 4 = 12 次！\n")

for i in range(1, 4):  # 外層迴圈：執行 3 次
    print(f"👉 外層第 {i} 次開始")
    for j in range(1, 4):  # 內層迴圈：執行 3 次
        print(f"   🔸 內層第 {j} 次，總第 {i*3 - 3 + j} 次")
    print(f"   👈 外層第 {i} 次結束\n")

# ============================================================
# 📐 理解外層與內層的概念
# ============================================================
print("\n" + "=" * 60)
print("📐 理解外層與內層的概念")
print("=" * 60)

print("""
🧠 記憶口訣：
   「外層動一次，內層跑到底」

就像時間表：
⏰ 外層：小時（1點、2點、3點...）
⏰ 內層：分鐘（每小時有60分鐘）

當前是 2點 的时候：
   要經過 2:00, 2:01, 2:02, ... 2:59
然後才進入下一個小時 3:00
""")

# 實際例子
print("\n時鐘比喻的程式碼：")
for hour in range(1, 4):  # 外層：小時
    print(f"⏰ 現在是 {hour} 點")
    for minute in range(0, 4):  # 內層：分鐘（只示範4分鐘）
        print(f"   🕐 {hour}:{minute}0")

# ============================================================
# 🎨 經典图案列印：直角三角形
# ============================================================
print("\n" + "=" * 60)
print("🎨 經典图案列印：直角三角形")
print("=" * 60)

print("""
我們要印出這樣的直角三角形：
*
**
***
****
*****

分析：
📐 外層迴圈：控制「行數」（有幾行）
📐 內層迴圈：控制「每行的星號數量」
""")

rows = 5
print(f"\nrows = {rows}，印出直角三角形：\n")

for row in range(1, rows + 1):  # 外層：控制行數
    line = ""
    for col in range(1, row + 1):  # 內層：控制每行的星號數
        line += "*"
    print(line)

# 另一種寫法：用乘法
print("\n另一種寫法：用 str * 次數：")
for row in range(1, rows + 1):
    print("*" * row)

# ============================================================
# 🔺 反向直角三角形
# ============================================================
print("\n" + "=" * 60)
print("🔺 反向直角三角形")
print("=" * 60)

print("""
印出反向的直角三角形：
*****
****
***
**
*

分析：
📐 外層迴圈：控制行數（第1行到第5行）
📐 內層迴圈：每行要印的星號數 = rows - row + 1
""")

rows = 5
print(f"\nrows = {rows}，印出反向直角三角形：\n")

for row in range(1, rows + 1):
    line = ""
    for col in range(1, rows - row + 2):
        line += "*"
    print(line)

# ============================================================
# 📩 長方形（矩形）图案
# ============================================================
print("\n" + "=" * 60)
print("📩 長方形（矩形）图案")
print("=" * 60)

height = 4
width = 7

print(f"\nheight = {height}, width = {width}，印出長方形：\n")

for row in range(1, height + 1):  # 外層：高度（行數）
    line = ""
    for col in range(1, width + 1):  # 內層：寬度（每行的字元數）
        line += "*"
    print(line)

# ============================================================
# 🔲 空心長方形
# ============================================================
print("\n" + "=" * 60)
print("🔲 空心長方形")
print("=" * 60)

height = 5
width = 8

print(f"\nheight = {height}, width = {width}，印出空心長方形：\n")

for row in range(1, height + 1):
    line = ""
    for col in range(1, width + 1):
        # 第一行、最後一行、每行的第一個、最後一個位置要印 *
        if row == 1 or row == height or col == 1 or col == width:
            line += "*"
        else:
            line += " "
    print(line)

# ============================================================
# 🔷 金字塔三角形
# ============================================================
print("\n" + "=" * 60)
print("🔷 金字塔三角形")
print("=" * 60)

rows = 5
print(f"\nrows = {rows}，印出金字塔：\n")

for row in range(1, rows + 1):
    # 每行：左邊空格 + 星號
    spaces = rows - row
    stars = 2 * row - 1
    line = " " * spaces + "*" * stars
    print(line)

# ============================================================
# 💻 二維串列（2D List）遍歷
# ============================================================
print("\n" + "=" * 60)
print("💻 二維串列（2D List）遍歷")
print("=" * 60)

print("""
巢狀迴圈最重要的應用之一：遍歷二維串列！

二維串列就像一張表格：
📊  row0: [國文, 數學, 英文]  → 第0列
📊  row1: [歷史, 物理, 化學]  → 第1列
📊  row2: [地理, 生物, 音樂]  → 第2列
""")

# 建立一個二維串列（成績表）
grades = [
    ["小明", 95, 88, 92],
    ["小華", 78, 85, 90],
    ["小美", 92, 94, 89],
    ["小強", 85, 76, 91]
]

print("學生成績表：")
print("-" * 40)

# 外層迴圈：遍歷每一列（每個學生的資料）
for i, student in enumerate(grades):
    print(f"📌 學生 {i}: {student[0]}", end=" → ")
    # 內層迴圈：遍歷每個學生的各科成績
    for j, score in enumerate(student[1:]):
        subject = ["國文", "數學", "英文"][j]
        print(f"{subject}:{score}", end=" ")
    print()

# ============================================================
# 🌍 實際應用範例：九九乘法表
# ============================================================
print("\n" + "=" * 60)
print("🌍 實際應用範例：九九乘法表")
print("=" * 60)

print("""
九九乘法表是巢狀迴圈最經典的應用！

📐 外層迴圈：乘法的「被乘數」（1到9）
📐 內層迴圈：乘法的「乘數」（1到9）
""")

print("\n📏 九九乘法表：\n")

for i in range(1, 10):  # 外層：被乘數
    line = ""
    for j in range(1, 10):  # 內層：乘數
        product = i * j
        line += f"{i}x{j}={product:2d}  "
    print(line)

# ============================================================
# 🎯 實際應用：座標系統
# ============================================================
print("\n" + "=" * 60)
print("🎯 實際應用：座標系統")
print("=" * 60)

print("""
在遊戲開發、影像處理中，
我們常常需要遍歷每一個「座標點」。

📍 外層迴圈：Y軸（行）
📍 內層迴圈：X軸（列）

就像掃描一張表格：
   X0  X1  X2  X3
Y0 [0] [1] [2] [3]
Y1 [4] [5] [6] [7]
Y2 [8] [9] [10][11]
""")

print("\n📍 3x4 網格的座標：\n")

rows = 3
cols = 4

for y in range(rows):  # 外層：Y軸
    line = ""
    for x in range(cols):  # 內層：X軸
        coord = f"({x},{y})"
        line += f"{coord:6s} "
    print(line)

# ============================================================
# 🧮 實際應用：找出質數
# ============================================================
print("\n" + "=" * 60)
print("🧮 實際應用：找出質數")
print("=" * 60)

print("""
質數：只能被 1 和自己整除的數

找出 1-20 之間的所有質數：
📐 外層迴圈：遍歷每個待測試的數
📐 內層迴圈：用 2 到 n-1 去除，看看能否整除
""")

print("\n🧮 1-20 的質數：")

prime_numbers = []

for num in range(2, 21):  # 外層：待測試的數
    is_prime = True
    for divisor in range(2, num):  # 內層：檢查是否有因數
        if num % divisor == 0:
            is_prime = False
            break  # 找到因數，不是質數，結束內層迴圈
    if is_prime:
        prime_numbers.append(num)

print(prime_numbers)

# ============================================================
# ⚠️ 巢狀迴圈的注意事項
# ============================================================
print("\n" + "=" * 60)
print("⚠️ 巢狀迴圈的注意事項")
print("=" * 60)

print("""
1️⃣ 避免無限迴圈！
   確保內層和外層的結束條件都能達到

2️⃣ 注意效能：
   巢狀迴圈會讓執行次數相乘
   外層 N 次 × 內層 M 次 = N × M 次執行
   如果 N=1000, M=1000，就是 1000000 次！

3️⃣ 變數命名：
   外層常用 i, j, k
   內層常見 i, j, k 的組合：
   - i(外層), j(內層)
   - row(外層), col(內層)
   - i, j, k（更多層巢狀）

4️⃣ break 和 continue：
   - break：只跳離當前所在的迴圈
   - continue：只跳過當前這次迭代
""")

