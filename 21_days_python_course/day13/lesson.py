print("""
我們用巢狀迴圈做一個小遊戲：
在同一張 5x5 的網格中，藏著一個「寶藏」
玩家有 3 次機會找到它！

📐 外層迴圈：Y軸（列）
📐 內層迴圈：X軸（行）
""")

import random

# 電腦選擇寶藏位置
treasure_x = random.randint(0, 4)
treasure_y = random.randint(0, 4)

print(f"🎯 寶藏藏在座標 ({treasure_x}, {treasure_y})")
print("請輸入 X (0-4) 和 Y (0-4) 來找寶藏！\n")

attempts = 3

for attempt in range(1, attempts + 1):
    print(f"--- 第 {attempt} 次機會 ---")
    
    # 顯示提示
    for y in range(5):
        line = ""
        for x in range(5):
            if x == treasure_x and y == treasure_y:
                line += "[？]"  # 寶藏位置（未揭曉）
            else:
                line += "[□]"  # 空白位置
        print(line)
    
    # 玩家猜測
    guess_x = int(input("輸入 X座標 (0-4): "))
    guess_y = int(input("輸入 Y座標 (0-4): "))
    
    if guess_x == treasure_x and guess_y == treasure_y:
        print("🎉 恭喜！找到寶藏了！")
        break
    else:
        # 給提示
        if guess_x < treasure_x:
            hint_x = "右邊"
        else:
            hint_x = "左邊"
        
        if guess_y < treasure_y:
            hint_y = "下面"
        else:
            hint_y = "上面"
        
        print(f"💡 提示：寶藏在你的{hint_x}、{hint_y}")
        print(f"❌ 不对！還有 {attempts - attempt} 次機會\n")

if guess_x != treasure_x or guess_y != treasure_y:
    print(f"😅 遊戲結束！正確答案是 ({treasure_x}, {treasure_y})")

# ============================================================
# 🎯 while 迴圈的巢狀結構
# ============================================================
print("\n" + "=" * 60)
print("🎯 while 迴圈的巢狀結構")
print("=" * 60)

print("""
巢狀迴圈不限於 for 迴圈，
while 迴圈也可以巢狀！

但要特別小心：
⚠️ 確保內層的條件最終會變成 False
   否則會變成無限迴圈！
""")

print("\n用 while 迴圈印出 3x3 格子：\n")

i = 1
while i <= 3:  # 外層 while
    j = 1
    line = ""
    while j <= 3:  # 內層 while
        line += f"[{i},{j}] "
        j += 1
    print(line)
    i += 1

