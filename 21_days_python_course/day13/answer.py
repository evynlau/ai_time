print("""
題目：
請用巢狀迴圈找出 1-100 之間的所有完美數。

完美數定義：「所有真因數的和等於自己」
6 = 1 + 2 + 3 是第一個完美數
28 = 1 + 2 + 4 + 7 + 14 是第二個完美數
""")

print("\n解答：\n")

perfect_numbers = []

for num in range(1, 101):
    divisor_sum = 0
    for divisor in range(1, num):
        if num % divisor == 0:
            divisor_sum += divisor
    if divisor_sum == num:
        perfect_numbers.append(num)

print("1-100之間的完美數：", perfect_numbers)

# 驗證一下
print("\n驗證：")
print("6 的因數：", [i for i in range(1, 6) if 6 % i == 0], "→ 總和 =", 1+2+3)
print("28 的因數：", [i for i in range(1, 28) if 28 % i == 0], "→ 總和 =", sum([i for i in range(1, 28) if 28 % i == 0]))

