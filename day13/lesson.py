# Day 13: 函数 - 重复使用的咒语
# 🎯 学习目标：理解函数的概念
#
# 📖 故事：每天问好!
# ❌ 不好的方法：写 3 行代码，晚上还要写一遍
# ✅ 好的方法：把问好变成咒语

# 定义咒语：创建一个函数
def say_greeting():
    print("👋 你好世界!")
    print("🌟 编程真有趣!")
    print("💪 继续学习!")

# 使用咒语!
print("🪄 第一次念咒语:")
say_greeting()

print()
print("🪄 第二次念咒语:")
say_greeting()

# 🎮 练习：创建你的咒语
def say_happy():
    print("😄 今天真开心")
    print("🎮 玩游戏时间")

say_happy()

# 🎯 小挑战：创建一个问候咒语
def say_goodnight():
    print("🌙 晚安，世界!")
    print("💤 好好休息")
    print("🌅 明天见!")

print()
say_goodnight()
