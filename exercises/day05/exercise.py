# Day 05 课后练习题 - 数学魔法棒
print("练习 1: 解决数学问题")

# 问题 1: 你有 10 颗糖，给妹妹 3 颗，还剩几颗？
candies = 10
give = 3
remaining = candies - give
print(f"\n🍬 还剩：{remaining} 颗糖")

# 问题 2: 你有 4 个盒子，每个盒子有 5 个苹果，共有多少个苹果？
boxes = 4
apples_per_box = 5
total_apples = boxes * apples_per_box
print(f"🍎 共有：{total_apples} 个苹果")

# 问题 3: 24 块饼干分给 6 个朋友，每人几块？
cookies = 24
friends = 6
cookies_per_friend = cookies / friends
print(f"🍪 每人：{cookies_per_friend} 块")

