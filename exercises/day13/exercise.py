# Day 13 课后练习题 - 函数练习
print("练习 1: 定义问候函数")

# 定义问候函数
def say_hello():
    print("👋 你好!")
    print("😊 很高兴见到你!")

# 使用函数
print("📝 第一次使用:")
say_hello()

print("\n📝 第二次使用:")
say_hello()

# 🎯 挑战：创建你自己的函数
print("\n🔧 创建你的问候函数:")

