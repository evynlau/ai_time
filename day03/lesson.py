# Day 03: 变量 - 给数据存储标签
# 🎯 学习目标：理解变量和赋值
#
# 📖 故事：小明有 3 个盒子！
# 📦 盒子 1: 标签"糖果" → 10 颗
# 🧸 盒子 2: 标签"玩偶" → 3 个
# 🎨 盒子 3: 标签"贴纸" → 50 张

# 变量就像有标签的盒子
candy = 10      # 盒子 1: candy
doll = 3        # 盒子 2: doll
sticker = 50    # 盒子 3: sticker

# 看看盒子里有什么
print(f"🍬 我有 {candy} 颗糖果")
print(f"🧸 我有 {doll} 个玩偶")
print(f"⭐ 我有 {sticker} 张贴纸")

# 🎮 练习：创建自己的盒子
name = "小明"
age = 8
print(f"👦 我叫 {name}, 今年 {age} 岁")

# 🎯 小挑战：创建 3 个你自己的变量
color = "蓝色"
favorite = "编程"
score = 100
print(f"🎨 颜色：{color}")
print(f"🎯 喜欢的：{favorite}")
print(f"📊 分数：{score}")
